        Remove-StaleGlobalPiCodingAgent -NpmExe $NpmExe
        Invoke-External -FilePath $NpmExe -Arguments @('install', '-g', $packageSpecifier, '--no-fund', '--no-audit')
    } -MaxAttempts 3 -DelaySeconds 5
}

function Invoke-WingetProcess {
    param(
        [Parameter(Mandatory = $true)][string]$WingetPath,
        [Parameter(Mandatory = $true)][string[]]$Arguments,
        [int[]]$AcceptableExitCodes = @()
    )

    Write-Info ("Run: " + $WingetPath + ' ' + ($Arguments -join ' '))
    $process = Start-Process -FilePath $WingetPath -ArgumentList $Arguments -Wait -NoNewWindow -PassThru
    $exitCode = $process.ExitCode
    if ($exitCode -ne 0 -and $AcceptableExitCodes -notcontains $exitCode) {
        throw "Command failed ($exitCode): $WingetPath $($Arguments -join ' ')"
    }
}

function Invoke-WingetInstall {
    param(
        [Parameter(Mandatory = $true)][string]$PackageId,
        [Parameter(Mandatory = $true)][string]$DisplayName
    )

    $winget = Get-CommandPathSafe -Name 'winget'
    if (-not $winget) {
        throw "'$DisplayName' is missing and winget is not available. Please install it manually."
    }

    Write-Step "Installing $DisplayName via winget"
    Invoke-WithRetry -Description "winget install $PackageId" -Action {
        Invoke-WingetProcess -WingetPath $winget -Arguments @(
            'install', '--id', $PackageId, '--exact', '--silent',
            '--accept-package-agreements', '--accept-source-agreements', '--disable-interactivity'
        )
    } -MaxAttempts 2 -DelaySeconds 5
    Refresh-ProcessPath
}

function Invoke-WingetUpgrade {
    param(
        [Parameter(Mandatory = $true)][string]$PackageId,
        [Parameter(Mandatory = $true)][string]$DisplayName
    )

    $winget = Get-CommandPathSafe -Name 'winget'
    if (-not $winget) {
        Write-Warning "winget is not available, so '$DisplayName' could not be checked for updates."
        return
    }

    # 0x8A15002B = WINGET_NO_APPLICABLE_UPGRADE (already up to date)
    $noUpgradeExitCode = -1978335189

    Write-Step "Checking for updates for $DisplayName via winget"
    try {
        Invoke-WithRetry -Description "winget upgrade $PackageId" -Action {
            Invoke-WingetProcess -WingetPath $winget -Arguments @(
                'upgrade', '--id', $PackageId, '--exact', '--silent',
                '--accept-package-agreements', '--accept-source-agreements', '--disable-interactivity',
                '--include-unknown'
            ) -AcceptableExitCodes @($noUpgradeExitCode)
        } -MaxAttempts 2 -DelaySeconds 5
        Refresh-ProcessPath
    }
    catch {
        Write-Warning "Could not update '$DisplayName' via winget: $($_.Exception.Message)"
    }
}

function Ensure-Command {
    param(
        [Parameter(Mandatory = $true)][string]$Name,
        [string]$WingetPackageId,
        [string]$DisplayName = $Name,
        [switch]$Optional
    )

    if (Test-CommandExists -Name $Name) {
        if ($WingetPackageId -and $UpdatePrerequisites) {
            Invoke-WingetUpgrade -PackageId $WingetPackageId -DisplayName $DisplayName
        }
        return (Get-CommandPathSafe -Name $Name)
    }

    if ($WingetPackageId) {
        try {
            Invoke-WingetInstall -PackageId $WingetPackageId -DisplayName $DisplayName
        }
        catch {
            if ($Optional) {
                Write-Warning $_.Exception.Message
                return $null
            }
            throw
        }

        if (Test-CommandExists -Name $Name) {
            return (Get-CommandPathSafe -Name $Name)
        }
    }

    if ($Optional) {
        Write-Warning "'$DisplayName' was not found."
        return $null
    }

    throw "'$DisplayName' was not found."
}

function Save-JsonObject {
    param(
        [Parameter(Mandatory = $true)][string]$Path,
        [Parameter(Mandatory = $true)]$Data
    )

    Ensure-Directory -Path (Split-Path -Parent $Path)
    $json = $Data | ConvertTo-Json -Depth 100
    $utf8NoBom = New-Object System.Text.UTF8Encoding($false)
    [System.IO.File]::WriteAllText($Path, ($json + "`n"), $utf8NoBom)
}

function ConvertTo-HashtableCompatible {
    param($InputObject)

    if ($null -eq $InputObject) {
        return $null
    }

    if ($InputObject -is [System.Collections.IDictionary]) {
        $result = [ordered]@{}
        foreach ($key in $InputObject.Keys) {
            $result[$key] = ConvertTo-HashtableCompatible -InputObject $InputObject[$key]
        }
        return $result
    }

    if (($InputObject -is [System.Collections.IEnumerable]) -and ($InputObject -isnot [string])) {
        $result = @()
        foreach ($item in $InputObject) {
            $result += ,(ConvertTo-HashtableCompatible -InputObject $item)
        }
        return $result
    }

    if ($InputObject -is [psobject]) {
        $properties = @($InputObject.PSObject.Properties | Where-Object { $_.MemberType -like '*Property' })
        if ($properties.Count -gt 0) {
            $result = [ordered]@{}
            foreach ($property in $properties) {
                $result[$property.Name] = ConvertTo-HashtableCompatible -InputObject $property.Value
            }
            return $result
        }
    }

    return $InputObject
}

function Load-JsonObject {
    param([Parameter(Mandatory = $true)][string]$Path)

    if (-not (Test-Path -LiteralPath $Path)) {
        return [ordered]@{}
    }

    $raw = Get-Content -LiteralPath $Path -Raw -Encoding UTF8
    if ([string]::IsNullOrWhiteSpace($raw)) {
        return [ordered]@{}
    }

    try {
        $convertFromJsonCommand = Get-Command ConvertFrom-Json -ErrorAction Stop
        if ($convertFromJsonCommand.Parameters.ContainsKey('AsHashtable')) {
            $parsed = $raw | ConvertFrom-Json -AsHashtable
        }
        else {
            $parsed = ConvertTo-HashtableCompatible -InputObject ($raw | ConvertFrom-Json)
        }

        if ($null -eq $parsed) {
            return [ordered]@{}
        }
        return $parsed
    }
    catch {
        throw "Could not parse JSON file '$Path': $($_.Exception.Message)"
    }
}

function Merge-UniqueStrings {
    param(
        $Existing,
        [string[]]$Incoming
    )

    $seen = [System.Collections.Generic.HashSet[string]]::new([System.StringComparer]::OrdinalIgnoreCase)
    $result = New-Object System.Collections.Generic.List[string]

    foreach ($item in @($Existing) + @($Incoming)) {
        if ($item -isnot [string]) { continue }
        if ([string]::IsNullOrWhiteSpace($item)) { continue }
        if ($seen.Add($item)) {
            [void]$result.Add($item)
        }
    }

    return @($result)
}

function Get-NpmExecutable {
    $npmCmd = Get-CommandPathSafe -Name 'npm.cmd'
    if ($npmCmd) { return $npmCmd }
    $npm = Get-CommandPathSafe -Name 'npm'
    if ($npm) { return $npm }
    return $null
}

function Get-PiExecutable {
    param([string]$NpmExe)

    if ($NpmExe) {
        $globalRoot = Get-GlobalNpmRoot -NpmExe $NpmExe
        if (-not [string]::IsNullOrWhiteSpace($globalRoot)) {
            $globalBin = Split-Path -Parent $globalRoot
            if (-not [string]::IsNullOrWhiteSpace($globalBin)) {
                $piCmd = Join-Path $globalBin 'pi.cmd'
                if (Test-Path -LiteralPath $piCmd) {
                    return $piCmd
                }
            }
        }
    }

    $appData = [Environment]::GetFolderPath('ApplicationData')
    $fallbackPiCmd = Join-Path $appData 'npm\pi.cmd'
    if (Test-Path -LiteralPath $fallbackPiCmd) {
        return $fallbackPiCmd
    }

    $piCmdResolved = Get-CommandPathSafe -Name 'pi.cmd'
    if ($piCmdResolved) { return $piCmdResolved }

    $pi = Get-CommandPathSafe -Name 'pi'
    if ($pi) { return $pi }

    return $null
}

function Get-GitBashPath {
    $candidates = New-Object System.Collections.Generic.List[string]

    foreach ($baseDir in @($env:ProgramFiles, ${env:ProgramFiles(x86)})) {
        if ([string]::IsNullOrWhiteSpace($baseDir)) { continue }
        $candidates.Add((Join-Path $baseDir 'Git\bin\bash.exe'))
    }

    $gitPath = Get-CommandPathSafe -Name 'git'
    if ($gitPath) {
        $gitCmdDir = Split-Path -Parent $gitPath
        $gitRootDir = Split-Path -Parent $gitCmdDir
        if (-not [string]::IsNullOrWhiteSpace($gitRootDir)) {
            $candidates.Add((Join-Path $gitRootDir 'bin\bash.exe'))
        }
    }

    foreach ($candidate in $candidates) {
        if (Test-Path -LiteralPath $candidate) {
            return $candidate
        }
    }

    $bash = Get-CommandPathSafe -Name 'bash'
    if ($bash) { return $bash }
    return $null
}

function Get-PowerShellExecutablePath {
    $currentShellPath = Get-CommandPathSafe -Name 'powershell'
    if ($currentShellPath) {
        return $currentShellPath
    }

    $systemRoot = [Environment]::GetEnvironmentVariable('SystemRoot')
    if (-not [string]::IsNullOrWhiteSpace($systemRoot)) {
        $fallback = Join-Path $systemRoot 'System32\WindowsPowerShell\v1.0\powershell.exe'
        if (Test-Path -LiteralPath $fallback) {
            return $fallback
        }
    }

    return 'powershell.exe'
}

