function Start-PiLoginIfNeeded {
    param(
        [Parameter(Mandatory = $true)][string]$PiExecutablePath,
        [Parameter(Mandatory = $true)][string]$WorkingDirectory,
        [Parameter(Mandatory = $true)][string]$StartScriptPath
    )

    if (Test-PiAuthenticated) {
        Write-Info 'Pi authentication already present. Skipping automatic login.'
        return
    }

    Write-Step 'Pi authentication required'
    Write-Warning 'No existing Pi authentication was found.'
    Write-Host 'Pi does not provide a non-interactive login flag for this flow.' -ForegroundColor Yellow
    Write-Host 'Start Pi and then run /login inside the Pi prompt.' -ForegroundColor Yellow
    Write-Host ''
    Write-Host 'Recommended command:' -ForegroundColor Yellow
    Write-Host ("  powershell -ExecutionPolicy Bypass -File `"{0}`"" -f $StartScriptPath) -ForegroundColor Yellow
    Write-Host 'Then type: /login' -ForegroundColor Yellow

    if (Test-Path -LiteralPath $StartScriptPath) {
        Write-Info 'Opening a new PowerShell window for Pi login.'
        try {
            Start-Process -FilePath (Get-PowerShellExecutablePath) -ArgumentList @(
                '-NoExit',
                '-ExecutionPolicy',
                'Bypass',
                '-File',
                $StartScriptPath
            ) | Out-Null
        }
        catch {
            Write-Warning "Could not open a new PowerShell window automatically: $($_.Exception.Message)"
        }
    }
}

function Install-TwinCATAdsPackage {
    param([Parameter(Mandatory = $true)][string]$PiExecutablePath)

    $source = if ([string]::IsNullOrWhiteSpace($TwinCATAdsSource)) {
        'npm:pi-twincat-ads'
    }
    else {
        (Resolve-Path -LiteralPath $TwinCATAdsSource -ErrorAction Stop).Path
    }

    $description = if ([string]::IsNullOrWhiteSpace($TwinCATAdsSource)) {
        'pi install npm:pi-twincat-ads'
    }
    else {
        'pi install local pi-twincat-ads source'
    }

    $label = if ([string]::IsNullOrWhiteSpace($TwinCATAdsSource)) {
        'Installing pi-twincat-ads from npm'
    }
    else {
        'Installing pi-twincat-ads from local path'
    }

    Write-Step $label
    Invoke-WithRetry -Description $description -Action {
        Invoke-External -FilePath $PiExecutablePath -WorkingDirectory $UserProfilePath -Arguments @('install', $source)
    } -MaxAttempts 3 -DelaySeconds 5
}

