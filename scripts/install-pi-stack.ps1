}

function Set-GeneratedAgentsSection {
    param(
        [Parameter(Mandatory = $true)][string]$Path,
        [Parameter(Mandatory = $true)][string]$GeneratedContent
    )

    $beginMarker = '<!-- BEGIN PI-SETUP-AUTOGENERATED -->'
    $endMarker = '<!-- END PI-SETUP-AUTOGENERATED -->'
    $block = $beginMarker + "`r`n" + $GeneratedContent.Trim() + "`r`n" + $endMarker + "`r`n"

    $existing = ''
    if (Test-Path -LiteralPath $Path) {
        $existing = Get-Content -LiteralPath $Path -Raw -Encoding UTF8
    }

    if ($existing -match [regex]::Escape($beginMarker) + '[\s\S]*?' + [regex]::Escape($endMarker)) {
        $updated = [regex]::Replace($existing, [regex]::Escape($beginMarker) + '[\s\S]*?' + [regex]::Escape($endMarker), $block.TrimEnd())
        Set-Content -LiteralPath $Path -Value ($updated.TrimEnd() + "`r`n") -Encoding UTF8
        return
    }

    $prefix = @"
# AGENTS.md

This file contains instructions for coding agents working in this repository.

"@

    if ([string]::IsNullOrWhiteSpace($existing)) {
        Set-Content -LiteralPath $Path -Value ($prefix + $block) -Encoding UTF8
    }
    else {
        $separator = if ($existing.EndsWith("`n")) { "" } else { "`r`n" }
        Set-Content -LiteralPath $Path -Value ($existing + $separator + "`r`n" + $block) -Encoding UTF8
    }
}

