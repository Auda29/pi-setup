    Backup-IfExists -Path $SettingsPath
    $settings = Load-JsonObject -Path $SettingsPath

    $settings['npmCommand'] = @('npm.cmd')
    $settings['shellPath'] = $gitBashPath
    $settings['sessionDir'] = '.pi/sessions'
