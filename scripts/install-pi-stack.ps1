}

function Invoke-External {
    param(
        [Parameter(Mandatory = $true)][string]$FilePath,
        [string[]]$Arguments = @(),
        [string]$WorkingDirectory
    )

    if ($WorkingDirectory) {
        Push-Location $WorkingDirectory
    }

    try {
        Write-Info ("Run: " + $FilePath + ' ' + ($Arguments -join ' '))
        & $FilePath @Arguments
        $exitCode = $LASTEXITCODE
        if ($exitCode -ne 0) {
            throw "Command failed ($exitCode): $FilePath $($Arguments -join ' ')"
        }
    }
    finally {
        if ($WorkingDirectory) {
            Pop-Location
        }
    }
}

function Invoke-WithRetry {
    param(
        [Parameter(Mandatory = $true)][scriptblock]$Action,
        [Parameter(Mandatory = $true)][string]$Description,
        [int]$MaxAttempts = 3,
        [int]$DelaySeconds = 4
    )

    $attempt = 0
    while ($attempt -lt $MaxAttempts) {
        $attempt++
        try {
            Write-Info "$Description (attempt $attempt/$MaxAttempts)"
            & $Action
            return
        }
        catch {
            if ($attempt -ge $MaxAttempts) {
                throw
            }
            Write-Warning "$Description failed: $($_.Exception.Message)"
            Write-Info "Waiting $DelaySeconds seconds before the next attempt ..."
            Start-Sleep -Seconds $DelaySeconds
        }
    }
}

function Get-NpmViewText {
    param(
        [Parameter(Mandatory = $true)][string]$NpmExe,
        [Parameter(Mandatory = $true)][string[]]$Arguments
    )

    $output = & $NpmExe @Arguments 2>$null
    if ($LASTEXITCODE -ne 0) {
        return $null
    }

    $text = ($output | Out-String).Trim()
    if ([string]::IsNullOrWhiteSpace($text)) {
        return $null
    }

    return $text
}

function Get-NpmViewJson {
    param(
        [Parameter(Mandatory = $true)][string]$NpmExe,
        [Parameter(Mandatory = $true)][string[]]$Arguments
    )

    $text = Get-NpmViewText -NpmExe $NpmExe -Arguments $Arguments
    if ([string]::IsNullOrWhiteSpace($text)) {
        return $null
    }

    try {
        return ($text | ConvertFrom-Json)
    }
    catch {
        return $null
    }
}

function Test-NpmPackageVersionExists {
    param(
        [Parameter(Mandatory = $true)][string]$NpmExe,
        [Parameter(Mandatory = $true)][string]$PackageName,
        [Parameter(Mandatory = $true)][string]$Version
    )

    $result = & $NpmExe 'view' ("$PackageName@$Version") 'version' '--json' 2>$null
    if ($LASTEXITCODE -ne 0) {
        return $false
    }

    $text = ($result | Out-String).Trim().Trim('"')
    return $text -eq $Version
}

function Get-GlobalNpmRoot {
    param([Parameter(Mandatory = $true)][string]$NpmExe)

    $text = Get-NpmViewText -NpmExe $NpmExe -Arguments @('root', '-g')
    if ([string]::IsNullOrWhiteSpace($text)) {
        return $null
    }

    return $text.Split([Environment]::NewLine)[0].Trim()
}

