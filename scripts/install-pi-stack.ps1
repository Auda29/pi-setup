            Write-Info "Removing stale path: $path"
            Remove-Item -LiteralPath $path -Recurse -Force -ErrorAction Stop
        }
        catch {
            Write-Warning "Could not remove stale path '$path': $($_.Exception.Message)"
        }
    }
}

