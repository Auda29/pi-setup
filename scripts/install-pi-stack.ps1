    Write-Info "Backup created: $target"
}

function Merge-UniqueStrings {
    param(
        [object[]]$Existing,
        [object[]]$Incoming
    )

    $list = New-Object System.Collections.Generic.List[string]
    $seen = [System.Collections.Generic.HashSet[string]]::new([System.StringComparer]::OrdinalIgnoreCase)

    foreach ($item in @($Existing) + @($Incoming)) {
        if ($null -eq $item) { continue }
        $text = [string]$item
        if ([string]::IsNullOrWhiteSpace($text)) { continue }
        if ($seen.Add($text)) {
            $list.Add($text)
        }
    }

    return @($list)
}

function Get-GitBashPath {
    $candidates = New-Object System.Collections.Generic.List[string]

    foreach ($baseDir in @($env:ProgramFiles, ${env:ProgramFiles(x86)})) {
        if ([string]::IsNullOrWhiteSpace($baseDir)) { continue }
        $candidates.Add((Join-Path $baseDir 'Git\bin\bash.exe'))
    }

    $gitPath = Get-CommandPathSafe -Name 'git'
    if ($gitPath) {
        $gitCmdDir = Split-Path -Parent $gitPath
        $gitRootDir = Split-Path -Parent $gitCmdDir
        if (-not [string]::IsNullOrWhiteSpace($gitRootDir)) {
            $candidates.Add((Join-Path $gitRootDir 'bin\bash.exe'))
        }
    }

    foreach ($candidate in $candidates) {
        if (Test-Path -LiteralPath $candidate) {
            return $candidate
        }
    }

    $bash = Get-CommandPathSafe -Name 'bash'
    if ($bash) { return $bash }
    return $null
}

function Get-PowerShellExecutablePath {
    $currentShellPath = Get-CommandPathSafe -Name 'powershell'
    if ($currentShellPath) {
        return $currentShellPath
    }

    $systemRoot = [Environment]::GetEnvironmentVariable('SystemRoot')
    if (-not [string]::IsNullOrWhiteSpace($systemRoot)) {
        $fallback = Join-Path $systemRoot 'System32\WindowsPowerShell\v1.0\powershell.exe'
        if (Test-Path -LiteralPath $fallback) {
            return $fallback
        }
    }

    return 'powershell.exe'
}

function Get-PiPackageSources {
    $sources = @()
    foreach ($name in $PackageNames) {
        $sources += ("npm:{0}" -f $name)
    }
    return $sources
}

function Test-PiAuthenticated {
    if (-not (Test-Path -LiteralPath $GlobalPiAgentAuthPath)) {
        return $false
    }

    try {
        $raw = Get-Content -LiteralPath $GlobalPiAgentAuthPath -Raw -Encoding UTF8
        return -not [string]::IsNullOrWhiteSpace($raw)
    }
    catch {
        Write-Warning "Could not inspect Pi auth state at '$GlobalPiAgentAuthPath': $($_.Exception.Message)"
        return $false
    }
}

function Start-PiLoginIfNeeded {
    param(
        [Parameter(Mandatory = $true)][string]$PiExecutablePath,
        [Parameter(Mandatory = $true)][string]$WorkingDirectory,
        [Parameter(Mandatory = $true)][string]$StartScriptPath
    )

    if (Test-PiAuthenticated) {
        Write-Info 'Pi authentication already present. Skipping automatic login.'
        return
    }

    Write-Step 'Pi authentication required'
    Write-Warning 'No existing Pi authentication was found.'
    Write-Host 'Pi does not provide a non-interactive login flag for this flow.' -ForegroundColor Yellow
    Write-Host 'Start Pi and then run /login inside the Pi prompt.' -ForegroundColor Yellow
    Write-Host ''
    Write-Host 'Recommended command:' -ForegroundColor Yellow
    Write-Host ("  powershell -ExecutionPolicy Bypass -File `"{0}`"" -f $StartScriptPath) -ForegroundColor Yellow
    Write-Host 'Then type: /login' -ForegroundColor Yellow

    if (Test-Path -LiteralPath $StartScriptPath) {
        Write-Info 'Opening a new PowerShell window for Pi login.'
        try {
            Start-Process -FilePath (Get-PowerShellExecutablePath) -ArgumentList @(
                '-NoExit',
                '-ExecutionPolicy',
                'Bypass',
                '-File',
                $StartScriptPath
            ) | Out-Null
        }
        catch {
            Write-Warning "Could not open a new PowerShell window automatically: $($_.Exception.Message)"
        }
    }
}

function Install-TwinCATAdsPackage {
    param([Parameter(Mandatory = $true)][string]$PiExecutablePath)

    $source = if ([string]::IsNullOrWhiteSpace($TwinCATAdsSource)) {
        'npm:pi-twincat-ads'
    }
    else {
        (Resolve-Path -LiteralPath $TwinCATAdsSource -ErrorAction Stop).Path
    }

    $description = if ([string]::IsNullOrWhiteSpace($TwinCATAdsSource)) {
        'pi install npm:pi-twincat-ads -l'
    }
    else {
        'pi install local pi-twincat-ads source -l'
    }

    $label = if ([string]::IsNullOrWhiteSpace($TwinCATAdsSource)) {
        'Installing pi-twincat-ads from npm'
    }
    else {
        'Installing pi-twincat-ads from local path'
    }

    Write-Step $label
    Invoke-WithRetry -Description $description -Action {
        Invoke-External -FilePath $PiExecutablePath -WorkingDirectory $ResolvedProjectRoot -Arguments @('install', $source, '--local')
    } -MaxAttempts 3 -DelaySeconds 5
}

function Install-PiPackages {
    param([Parameter(Mandatory = $true)][string]$PiExecutablePath)

    Write-Step 'Installing local Pi packages via pi install'
    foreach ($source in Get-PiPackageSources) {
        Invoke-WithRetry -Description ("pi install {0} -l" -f $source) -Action {
            Invoke-External -FilePath $PiExecutablePath -WorkingDirectory $ResolvedProjectRoot -Arguments @('install', $source, '--local')
        } -MaxAttempts 3 -DelaySeconds 5
    }
}

